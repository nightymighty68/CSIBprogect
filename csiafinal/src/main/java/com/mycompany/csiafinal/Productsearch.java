    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      String gt = jTextField1.getText();
        int qu = Integer.parseInt(jTextField2.getText());
        double price = Double.parseDouble(jTextField4.getText());
        String mi = String.valueOf(jTextArea1.getText());
        
        int adduser = JOptionPane.showConfirmDialog( null, "CONFIRM DELETE","YES",WIDTH);
        if(adduser== JOptionPane.YES_OPTION){
            try{
                String SQL = "delete FROM ADDPRODUCT WHERE GASTYPE='" + gt + "'";
                pst = conn.prepareStatement(SQL);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null , "Product successfully Deleted");
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

